function Val(str){return Math.floor(str)}
function Rnd(){return Math.random()}
function Asc(str){return str.charCodeAt(str)}
function Split(str,slt){return str.split(slt)}
function Instr(str,cpr){return str.indexOf(cpr)}
function Chr(str){return String.fromCharCode(str)}
function Mid(str,fst,val){return str.substr(fst,val)}
function Controls(id){return document.getElementById(id)}