function Rnd(){return Math.random()}
function Val(val){return Math.floor(val)}
function Asc(str){return str.charCodeAt(str)}
function Split(str,sym){return str.split(sym)}
function Instr(str,cpr){return str.indexOf(cpr)}
function Chr(val){return String.fromCharCode(val)}
function Mid(str,srt,val){return str.substr(srt,val)}
function Controls(id){return document.getElementById(id)}